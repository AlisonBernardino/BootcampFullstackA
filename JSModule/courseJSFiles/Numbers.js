// Numbers
// Attribution and division
let baseValue = 150;
let denominator = 3;
let divisionResult = baseValue / denominator;
console.log(`${baseValue} / ${denominator} = ${divisionResult}`);

// Multiplication
let multiplier = 4;
let multiplicationResult = baseValue * multiplier;
console.log(`${baseValue} x ${multiplier} = ${multiplicationResult}`);

// .floor method
